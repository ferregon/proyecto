    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
   String ruta,nombre;
        final JFileChooser elegirImagen = new JFileChooser();
        elegirImagen.setMultiSelectionEnabled(false);
        int o = elegirImagen.showOpenDialog(this);
        if(o == JFileChooser.APPROVE_OPTION){
            ruta = elegirImagen.getSelectedFile().getAbsolutePath();
            nombre = elegirImagen.getSelectedFile().getName();
            txtnomimagen.setText(ruta);
            Image preview = Toolkit.getDefaultToolkit().getImage(ruta);
            if(preview != null){
                jLabel4.setText("");
                ImageIcon icon = new ImageIcon(preview.getScaledInstance(jLabel4.getWidth(), jLabel4.getHeight(), Image.SCALE_DEFAULT));
                jLabel4.setIcon(icon);
            }
        }
       // TODO add your handling code here:
    }//GEN-LAST:event_jLabel4MouseClicked

    private void tbusuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbusuarioMouseClicked
        // TODO add your handling code here:
        if(evt.getButton()==1){
            try{
                conectar cc = new conectar();
                Connection cn = cc.conexion();
                desbloquear();
                InputStream is;
                int fila=tbusuario.getSelectedRow();
                String sql="select * from tcliente where cedula="+tbusuario.getValueAt(fila,0);
                Statement sent = cn.createStatement();
                ResultSet rs=sent.executeQuery(sql);
                rs.next();
                txtcedula.setText(rs.getString("cedula"));
                txtruc.setText(rs.getString("ruc"));
                txtnom.setText(rs.getString("nombre"));
                txtdirec.setText(rs.getString("direccion"));
                txttelef.setText(rs.getString("telefono"));
                txtnomimagen.setText(rs.getString("ruta"));
               Image i=null;
                Blob blob=rs.getBlob("foto");
                i=javax.imageio.ImageIO.read(blob.getBinaryStream());
                ImageIcon image=new ImageIcon(i.getScaledInstance(jLabel4.getWidth(), jLabel4.getHeight(), Image.SCALE_REPLICATE));
                jLabel4.setIcon(image);
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_tbusuarioMouseClicked

